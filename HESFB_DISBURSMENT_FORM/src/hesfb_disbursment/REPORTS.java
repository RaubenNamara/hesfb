    private void print1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_print1ActionPerformed
        String regno = t1.getText();
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/myhesfb","root","");
            Statement st = con.createStatement();
            String sql = "SELECT * FROM myhesfb.student_data WHERE REG_NO = '"+regno+"'";
            ResultSet rs = st.executeQuery(sql);
            
            DefaultTableModel model = (DefaultTableModel) tb.getModel();
            model.setRowCount(0);
            if(rs.next()){
                String a = rs.getString("NAME");              
                String b = rs.getString("GENDER");
                String c = rs.getString("REG_NO");
                String d = rs.getString("EMAIL_ADDRESS");
                String e = rs.getString("COURSE");
                String f = rs.getString("NATIONAL_ID");
                String g = rs.getString("BIRTH_DATE");               
                String h = rs.getString("PASSWORD");
                
                String arr[] = {a,b,c,d,e,f,g,h};
                model.addRow(arr);
            }
            else{
                 JOptionPane.showMessageDialog(this,"Record not found");
                    t1.setText("");
                    t1.requestFocus();
                    
                    
                    }
        } catch (SQLException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(this,"something went wrong");
            Logger.getLogger(REPORTS.class.getName()).log(Level.SEVERE, null, ex);
            
        }
    }//GEN-LAST:event_print1ActionPerformed

